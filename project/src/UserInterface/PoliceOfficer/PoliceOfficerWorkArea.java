    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int row = tblMyWorkRequests.getSelectedRow();
        
        if (row < 0){
            JOptionPane.showMessageDialog(null,"Please select the work-request first");
            return;
        }
        
        WorkRequest request = (WorkRequest) tblMyWorkRequests.getModel().getValueAt(row, 5);
        
        String input = JOptionPane.showInputDialog("Please enter the message for Hospital Processor:");
        request.setMsgHosp(input);
        
        int myX = request.getSender().getCurrentLocation().getLocationX();
        int myY = request.getSender().getCurrentLocation().getLocationY();
        int result =150;
        Organization org = null;
        UserAccount resultAccount = null;
        
        for(Network n : system.getNetworkList()){
            for(Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()){
                if(e instanceof HospitalEnterprise){
                    for(Organization o : e.getOrganizationDirectory().getOrganizationList())
                        if(o instanceof HospitalProcessorOrganization){
                            for(UserAccount ua: o.getUserAccountDirectory().getUserAccountList()){
                                if(ua.getIsActive().equals("yes")){
                                int tempX = ua.getCurrentLocation().getLocationX();
                                int tempY = ua.getCurrentLocation().getLocationY();
                                
                                int temp =0;
                                
                                temp =(int) Math.sqrt((myX-tempX)*(myX-tempX) + (myY-tempY)*(myY-tempY));
                                if(temp<result){
                                    result=temp;
                                    org=o;
                                    resultAccount = ua;
                                }
                            }
                            }
//                            request.setCaseSatus("Under Hospital Dept");
                            //o.getWorkQueue().getWorkRequestList().add(request);
                            //populateCrimeTable();
                            //populateMyWorkRequestTable();
                        }
                }
            }
        }
        
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(request);
            
            //request.setReceiver(resultAccount);
            //request.setCaseSatus("WithHospital");
            
            
            resultAccount.getWorkQueue().getWorkRequestList().add(request);
            //account.getWorkQueue().getWorkRequestList().add(workRequest);
            JOptionPane.showMessageDialog(null,"Message sent to nearest Hospital Processor!");
            populateCrimeTable();
            //txtAddress.setText("");
            populateMyWorkRequestTable();
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

