    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = userJTable.getSelectedRow();
        
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null,"Please select the User Account from the list");
            return;
        }
        
        UserAccount ua = (UserAccount)userJTable.getValueAt(selectedRow, 0);
        
        int dialogResult = JOptionPane.showConfirmDialog (null, "Change the status of selected user account?","Warning",JOptionPane.YES_NO_OPTION);
        if(dialogResult == JOptionPane.YES_OPTION){
            if(ua.getIsActive().equals("yes"))
                ua.setIsActive("no");
            else
                ua.setIsActive("yes");
           }
        popData();
        
    }//GEN-LAST:event_jButton1ActionPerformed

