    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        WorkRequest.Type type = (WorkRequest.Type)cmbCrimeType.getSelectedItem();
        String message = txtDescription.getText().trim();
        
        Date crimeDate = dateCrimeDate.getDate();
        String address = txtAddress.getText().trim();
        
        if(message.equals("") || crimeDate ==null || address.equals("")){
            JOptionPane.showMessageDialog(null,"Please enter all the details.");
            return;
        }
        
        WorkRequest workRequest = new WorkRequest();
        workRequest.setCaseDetails(message);
        workRequest.setSender(account);
        workRequest.setCrimeType(type.getValue());
        workRequest.setReportingAddress(address);
        workRequest.setCaseSatus(WorkRequest.Status.AwaitingResponse.getValue());
        workRequest.setCrimeDate(crimeDate);
        
        int myX = account.getCurrentLocation().getLocationX();
        int myY = account.getCurrentLocation().getLocationY();
        int result =150;
        Organization org = null;
        UserAccount resultAccount = null;
        
        
        
        for(Network n : system.getNetworkList()){
            for(Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()){
                if(e instanceof PoliceEnterprise){
                    for(Organization o: e.getOrganizationDirectory().getOrganizationList()){
                        if(o instanceof PoliceOfficerOrganization){
                            for(UserAccount ua: o.getUserAccountDirectory().getUserAccountList()){
                                if(ua.getIsActive().equals("yes")){
                                int tempX = ua.getCurrentLocation().getLocationX();
                                int tempY = ua.getCurrentLocation().getLocationY();
                                
                                int temp =0;
                                
                                temp =(int) Math.sqrt((myX-tempX)*(myX-tempX) + (myY-tempY)*(myY-tempY));
                                if(temp<result){
                                    result=temp;
                                    org=o;
                                    resultAccount = ua;
                                }
                            }
                            }
                        }
                    }
                }
            }
        }
        

        /*for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof PoliceOfficerOrganization){
                org = organization;
                break;
            }
        }*/
        
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(workRequest);
            
            workRequest.setReceiver(resultAccount);
            workRequest.setCaseSatus(WorkRequest.Status.WithPoliceDept.getValue());
            
            resultAccount.getWorkQueue().getWorkRequestList().add(workRequest);
            account.getWorkQueue().getWorkRequestList().add(workRequest);
            JOptionPane.showMessageDialog(null,"Request submitted successfully!");
            populateMyRequests();
            txtAddress.setText("");
            txtDescription.setText("");
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

