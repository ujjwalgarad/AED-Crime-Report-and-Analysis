    public void populateMyWorkRequestTable(){
        DefaultTableModel dtm = (DefaultTableModel)tblMyWorkRequests.getModel();
        
        dtm.setRowCount(0);
        
        for(WorkRequest request : account.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[7];
            
            row[0] = request.getSender().getEmployee().getName();
            row[1] = request.getReceiver().getUsername();
            row[2] = request.getCaseSatus();
            row[3] = request.getCrimeDate();
            row[4] = request.getResolveDate();
            row[6] = request.getCrimeType();
            row[5] = request;
            dtm.addRow(row);
        }
    }
    public void populateRegionCombo(){
        //cmbPoliceRegion.removeAllItems();
        for(Network n : system.getNetworkList()){
            for(Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()){
                    if(e instanceof PoliceEnterprise){
                        cmbPoliceRegion.addItem(e);
                    }
                }
            }
        cmbPoliceRegion.addItem("All");
    }
