    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed
        // TODO add your handling code here:
        String result = txtCaseNote.getText();
        //String status = cmbCaseStatus.getSelectedItem();
        WorkRequest.Status status = (WorkRequest.Status)cmbCaseStatus.getSelectedItem();
        
        request.getCrimeCase().setCaseNote(result);
        request.setCaseSatus(status.getValue());
        UserAccount sender = request.getSender();
        request.setResolveDate(dateResolve.getDate());
        
        /*for(WorkRequest userReq : sender.getWorkQueue().getWorkRequestList()){
            if (request.getWorkReqId()== userReq.getWorkReqId()){
                userReq.setCaseSatus(status.getValue());
            }
        }*/
        
        if(status.getValue().equals("Needs Thorough Investigation")){
            for(Organization o : enterprise.getOrganizationDirectory().getOrganizationList()){
                if(o instanceof EnterpriseHeadOrganization){
                    for(UserAccount ua : o.getUserAccountDirectory().getUserAccountList()){
                        request.setReceiver(ua);
                        ua.getWorkQueue().getWorkRequestList().add(request);
                        account.getWorkQueue().getWorkRequestList().remove(request);
                    }
                }
            }
        }
        JOptionPane.showMessageDialog(null,"Work request processed successfully!");
        
    }//GEN-LAST:event_btnProcessActionPerformed

