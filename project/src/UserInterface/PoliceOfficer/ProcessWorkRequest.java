    public void populateFields(){
        txtAddress.setText(request.getReportingAddress());
        txtCrimeDate.setText(String.valueOf(request.getCrimeDate()));
        txtDescription.setText(request.getCaseDetails());
        txtCrimeType.setText(request.getCrimeType());
        
        
        cmbCaseStatus.removeAllItems();
        
        for (WorkRequest.Status status : WorkRequest.Status.values()){
            if(!status.equals("AwaitingResponse") ||
                    !status.equals("WithPoliceDept"))
            cmbCaseStatus.addItem(status);
        }
        
        populateVictims();
        populateSuspects();
    }
    
    public void populateVictims(){
        Case c = request.getCrimeCase();
        DefaultTableModel dtm = (DefaultTableModel)tblVictims.getModel();        
        dtm.setRowCount(0);
        
        for(Victim v : c.getVictimList()){
            Object[] row = new Object[2];   
            row[0] = v.getVictimName();
            row[1] = v.getVictimAge();
            dtm.addRow(row);
        }
    }
    
    public void populateSuspects(){
        Case c = request.getCrimeCase();
        DefaultTableModel dtm = (DefaultTableModel)tblSuspects.getModel();        
        dtm.setRowCount(0);
        
        for(Suspect s : c.getSuspectList()){
            Object[] row = new Object[2];   
            row[0] = s.getSuspectName();
            row[1] = s.getSuspectAge();
            dtm.addRow(row);
        }
    }
