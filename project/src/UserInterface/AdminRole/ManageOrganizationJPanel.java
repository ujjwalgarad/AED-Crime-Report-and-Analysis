    private void populateCombo(){
        organizationJComboBox.removeAllItems();
        if(enterprise instanceof PoliceEnterprise){
        //    for(Organization o : enterprise.getOrganizationDirectory().getOrganizationList()){
        for (Type type : Organization.Type.values()){
            if (type.getValue().equals(Type.RequesterOrg.getValue()) ||
                    type.getValue().equals(Type.PoliceOfficerOrg.getValue()) ||
                    type.getValue().equals(Type.EnterpriseHeadOrg.getValue()))
                organizationJComboBox.addItem(type);
            }
        }
        
        if(enterprise instanceof HospitalEnterprise){
        for (Type type : Organization.Type.values()){
            if (type.getValue().equals(Type.HospitalProcessorOrg.getValue()))
                organizationJComboBox.addItem(type);
            }
        }
        
        if(enterprise instanceof FirefighterEnterprise){
        for (Type type : Organization.Type.values()){
            if (type.getValue().equals(Type.FireFighterProcessorOrg.getValue()))
                organizationJComboBox.addItem(type);
            }
        }
    }

    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();
        
        model.setRowCount(0);
        
        for (Organization organization : directory.getOrganizationList()){
            Object[] row = new Object[2];
            row[0] = organization.getOrganizationID();
            row[1] = organization.getName();
            
            model.addRow(row);
        }
    }
