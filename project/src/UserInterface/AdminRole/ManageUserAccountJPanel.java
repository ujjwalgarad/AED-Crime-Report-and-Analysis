    private void createUserJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUserJButtonActionPerformed
        //try{
            String userName = nameJTextField.getText().trim();
        String password = passwordJTextField.getText().trim();
        
        if(userName.equals("") || password.equals("")){
            JOptionPane.showMessageDialog(null,"Please enter valid input.");
            return;
        }
        Organization organization = (Organization) organizationJComboBox.getSelectedItem();
        Employee employee = (Employee) employeeJComboBox.getSelectedItem();
        Role role = (Role) roleJComboBox.getSelectedItem();
        
        if(organization.getUserAccountDirectory().checkIfUsernameIsUnique(userName, system) == false){
            JOptionPane.showMessageDialog(null,"Username already exists, please select another username");
            return;
        }else{
        UserAccount account = organization.getUserAccountDirectory().createUserAccount(userName, password, employee, role);
        
        if(radYes.isSelected())
        account.setIsActive("yes");
        else
            account.setIsActive("no");
        
        JOptionPane.showMessageDialog(null,"User account created");
        nameJTextField.setText("");
        passwordJTextField.setText("");
        popData();
        }
        //}catch (Exception e){
         //   JOptionPane.showMessageDialog(null,"Please enter valid input.");
        //    return;
       // }
    }//GEN-LAST:event_createUserJButtonActionPerformed

