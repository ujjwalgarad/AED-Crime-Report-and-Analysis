    public void populateMyRequests(){
        DefaultTableModel dtm = (DefaultTableModel)tblCrimeTable.getModel();
        
        dtm.setRowCount(0);
        
        for(WorkRequest request : account.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[6];
            
            
            row[0] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();
            row[1] = request.getCaseSatus();
            row[2] = request.getCrimeDate();
            row[3] = request.getResolveDate();
            row[5] = request.getCrimeType();
            row[4] = request;
            
            dtm.addRow(row);
        }
    }
    
    public void populateCrimeTypeCombo(){
        
        cmbCrimeType.removeAllItems();
        
        for (WorkRequest.Type type : WorkRequest.Type.values()){
            cmbCrimeType.addItem(type);
        }
    }
    
