    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(txtSearch.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Please enter Crime Id for search");
            return;
        }else{
            int searchId = Integer.parseInt(txtSearch.getText().trim());
            
            for(Network n : system.getNetworkList()){
                for(Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()){
                    if(e instanceof PoliceEnterprise){
                        for(Organization o : e.getOrganizationDirectory().getOrganizationList()){
                            if(o instanceof PoliceOfficerOrganization){
                                for(WorkRequest w: o.getWorkQueue().getWorkRequestList()){
                                    if(searchId == w.getWorkReqId()){
                                        SearchResultDeptHead sr = new SearchResultDeptHead(w, upc,system,account,enterprise,this);
                                        upc.add("searchResult", sr);
                                        CardLayout layout = (CardLayout) upc.getLayout();
                                        layout.next(upc);
                            }
                        }
                    }
                }
            }
            }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

