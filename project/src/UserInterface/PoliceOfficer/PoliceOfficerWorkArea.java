    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
        int searchId = Integer.parseInt(txtSearch.getText().trim());
        
        if(txtSearch.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Please enter Crime Id for search");
            return;
        }else{
            
            
            for(Network n : system.getNetworkList()){
                for(Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()){
            if(e instanceof PoliceEnterprise){
                for(Organization o : e.getOrganizationDirectory().getOrganizationList()){
                    if(o instanceof PoliceOfficerOrganization){
                        for(WorkRequest w: o.getWorkQueue().getWorkRequestList()){
                            if(searchId == w.getWorkReqId()){
                                SearchResult sr = new SearchResult(w, userPRocessContainer,system,account,organization,enterprise,this);
                                userPRocessContainer.add("searchResult", sr);
                                CardLayout layout = (CardLayout) userPRocessContainer.getLayout();
                                layout.next(userPRocessContainer);
                            }
                        }
                    }
                }
            }
                }
            }
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Please enter valid input");
            return;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

