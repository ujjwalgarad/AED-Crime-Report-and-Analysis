    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed

        Type type = (Type) organizationJComboBox.getSelectedItem();
        
        for(Organization o : enterprise.getOrganizationDirectory().getOrganizationList()){
            if(o instanceof DeptHeadOrganization){
                if(type.getValue().equals("DeptHeadOrg")){
                    JOptionPane.showMessageDialog(null,"Organization of same type already exists, can't create another one.");
                    return;
                }
            }
            
            if(o instanceof EnterpriseHeadOrganization){
                if(type.getValue().equals("EnterpriseHeadOrg")){
                    JOptionPane.showMessageDialog(null,"Organization of same type already exists, can't create another one.");
                    return;
                }
            }
            
            if(o instanceof HospitalProcessorOrganization){
                if(type.getValue().equals("HospitalProcessorOrg")){
                    JOptionPane.showMessageDialog(null,"Organization of same type already exists, can't create another one.");
                    return;
                }
            }
            
            if(o instanceof FirefighterProcessorOrganization){
                if(type.getValue().equals("FireFighterProcessorOrg")){
                    JOptionPane.showMessageDialog(null,"Organization of same type already exists, can't create another one.");
                    return;
                }
            }
            
            if(o instanceof PoliceOfficerOrganization){
                if(type.getValue().equals("PoliceOfficerOrg")){
                    JOptionPane.showMessageDialog(null,"Organization of same type already exists, can't create another one.");
                    return;
                }
            }
            
            if(o instanceof RequesterOrganization){
                if(type.getValue().equals("RequesterOrg")){
                    JOptionPane.showMessageDialog(null,"Organization of same type already exists, can't create another one.");
                    return;
                }
            }
        }
        
        directory.createOrganization(type);
        populateTable();
    }//GEN-LAST:event_addJButtonActionPerformed

